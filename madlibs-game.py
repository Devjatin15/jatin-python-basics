# madlibs game 
# A madlibs game is a fun word game where players fill in the blanks of a story with different types of words (nouns, verbs, adjectives, etc.) to create a humorous or nonsensical story. Here's a simple implementation of a madlibs game in Python:

adjective1 = input("Enter an adjective: ")
noun1 = input("Enter a noun: ")
adjective2 = input("Enter another adjective: ")
verb1 = input("Enter a verb: ")
adjective3 = input("Enter one more adjective: ")


print(f"today i went to a {adjective1} zoo")
print(f"in an exhibit i saw a {noun1}")
print(f"{noun1} was {adjective2} and {verb1}")
print(f"i was {adjective3}")